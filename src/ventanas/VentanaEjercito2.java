    private void updateComponentState() {
        
        this.buttonConfirmar.setEnabled(batalla.getEjercito2().valido());
        List<String[]> listArray = new ArrayList<>();
        for(Componente c : this.batalla.getEjercito2().getComponentes()){
            listArray.add(c.toStringArray());
        }
        
        //System.out.println(this.batalla.getEjercito2().getComponentes().size() + " Unidades");
        
        
        String[][] tableDataModel = listArray.stream().toArray(String[][]::new);
        
        //System.out.println(tableDataModel);
        
        this.jTable2.setModel(new DefaultTableModel(
                tableDataModel,
                new String [] {"Tipo", "Nombre", "Ataque", "Defensa", "Salud"}
        ));
        
        this.jLabelsalud.setText("Salud Total: " + this.batalla.getEjercito2().getSalud());
        this.jLabelTotal.setText("Peso Total: " + this.batalla.getEjercito2().getSaldoPeso());
        
        pack();
    }
