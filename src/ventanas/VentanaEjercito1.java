    private void buttonSleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSleccionarActionPerformed
  
        try {
            if (radioButtonAnadirInfanteria.isSelected()) this.batalla.getEjercito1().agregarComponente(new Infanteria());
            if (radioButtonAnadirCaballeria.isSelected()) this.batalla.getEjercito1().agregarComponente(new Caballeria());
            if (radioButtonAnadirGeneral.isSelected()) this.batalla.getEjercito1().agregarComponente(this.mysql.getRandomGeneral());
            if (radiobuttonAnadirElefante.isSelected()) this.batalla.getEjercito1().agregarComponente(new Elefante());
            if (radioButtonAnadirTigre.isSelected()) this.batalla.getEjercito1().agregarComponente(new Tigre());
            if (radioButtonEliminarUnidad.isSelected()){
                final int fila = jTable2.getSelectedRow();
                final String nombre = (String) jTable2.getModel().getValueAt(fila, 1);
                this.batalla.getEjercito1().eliminarComponenteConNombre(nombre);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        } finally {
            this.updateComponentState();
        }
    }//GEN-LAST:event_buttonSleccionarActionPerformed

